        <h3 id="A">Accelerated Death Benefit</h3>
        
        <p>A life insurance policy feature that lets you use some of the policy’s death benefit prior to death.</p>
        
        <h3>Activities of Daily Living (ADLs)</h3>
        
        <p>Activities of daily living are the everyday, basic activities that most people must do as a necessity of living: bathing, dressing, toileting, transferring (moving to and from a bed or a chair), eating, and caring for incontinence. A person’s need for help with these activities or inability to perform them is used as a means of determining eligibility for public programs such as Medicaid long-term care services. Also, many long-term care insurance policies use the inability to do a certain number of activities of daily living as criteria for paying benefits.</p>
        
        <h3>Adult Day Services</h3>
        
        <p>Services provided during the day at a community center. Programs are designed to meet the needs of adults with physical or mental disabilities through an individual plan of care. These programs provide a variety of social and support services in a protective setting during any part of a day, but less than 24-hour care. Many adult day service programs include health-related services.</p>
        
        <h3>Adult Foster Care</h3>
        
        <p>This is a 24-hour living arrangement with supervision for people who are unable to continue living independently in their own homes because of physical, mental, or emotional limitations. Those who provide adult foster care services live in the same household with residents and share a common living area.</p>
        
        <h3>Advance Care Planning</h3>
        
        <p>Learning and planning for end-of-life care options and services.</p>
        
        <h3>Advance Directive</h3>
        
        <p>Legal document used to tell medical professionals whether you would like to be kept on artificial life support if you become permanently unconscious or are dying and unable to speak for yourself. It also specifies other aspects of health care you would like under those circumstances. Also called health care directive, advanced health care directive or living will.</p>
        
        <h3>Alzheimer’s Disease</h3>
        
        <p>Progressive, degenerative form of dementia that causes severe intellectual deterioration; first symptoms are impaired memory, which is followed by impaired thought and speech.</p>
        
        <h3>Annuities/Annuity Contract</h3>
        
        <p>A contract – usually sold by an insurance company – that provides the buyer or a designated party a series of regular payments over a specified and defined period of time.</p>
        
        <h3>Assisted Living Facility</h3>
        
        <p>Residential living arrangement that provides personal care, assistance with activities of daily living, help with medications, and services such as laundry and housekeeping. Facilities may also provide health and medical care, but care is not as intensive as care offered at a nursing home. Assisted living facilities allow people to remain relatively independent. Types and sizes of facilities vary, ranging from small homes to large apartment-style complexes. Levels of care and services also vary.</p>
        
        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="B">Benefit Triggers (Triggers)</h3>
        
        <p>Criteria insurance companies use to determine when you are eligible to receive benefits. The most common benefit triggers for long-term care insurance are needing help with two or more activities of daily living or having a cognitive impairment.</p>

        <h3>Benefit Waiting Period</h3>
        
        <p>Specified amount of time at the beginning of a disability during which covered services are received, but for which the policy will not pay benefits. Also known as deductible period or elimination period.</p>

        <h3>Benefits</h3>
        
        <p>Money paid by an insurance company to a policyholder or to a care provider for services covered by the insurance policy.</p>

        <h3>Board and Care Home</h3>
        
        <p>Private homes that provide housing, meals, housekeeping, personal care services, and support to residents who are frail or have a disability. At least one caregiver is required to be present at all times. In many states, board and care homes are licensed or certified and must meet criteria for facility safety, types of services provided, and the number and type of residents for which they can care. Board and care homes are often owned and managed by an individual or family that is involved in the everyday operation of the home. Also called group homes or residential care facilities.</p>
        
        <p class="arrow"><a href="#content">Back to Top</a></p>

        <h3 id="C">Cardiopulmonary Resuscitation (CPR)</h3>

        <p>Combination of rescue breathing (mouth-to-mouth resuscitation) and chest compressions used if someone isn’t breathing or circulating blood adequately. CPR can restore circulation of oxygen-rich blood to the brain.</p>

        <h3>Caregiver</h3>
        
        <p>A caregiver is a family member, partner, friend, or neighbor who helps care for someone who is elderly or has a disability and lives at home.</p>

        <h3>Charitable Remainder Trust</h3>
        
        <p>Special tax-exempt irrevocable trust written to comply with federal tax laws and regulations. With this kind of trust, you transfer cash or assets into the trust and may receive some income from it for the rest of your life or a specified number of years (not to exceed 20). The minimum payout rate is 5 percent and the maximum is 50 percent. When you die, the remaining amount in the trust goes to the charity that was designated to receive it as part of the trust arrangement.</p>

        <h3>Chronic Illness</h3>
        
        <p>A long-lasting or recurrent illness or condition that causes a person to need help with activities of daily living and often other health and support services. Examples include Parkinson’s disease or Alzheimer’s disease. At a minimum, the condition is expected to last for at least 90 consecutive days. This term is used in a tax-qualified long-term care insurance policy to describe a person who needs long-term care because of an inability to do a certain number of activities of daily living without help or because of a severe cognitive impairment.</p>

        <h3>Cognitive Impairment</h3>
        
        <p>Deficiency in certain functions of the brain – short or long-term memory; orientation to person, place, and time; deductive or abstract reasoning; or judgment as it relates to safety awareness. An example of a cognitive impairment is Alzheimer’s disease.</p>

        <h3>Community-Based Services</h3>
        
        <p>Services in the community, such as adult day services, home delivered meals, or transportation services.</p>

        <h3>Continuing Care Retirement Communities (CCRC)</h3>
        
        <p>Retirement complex that offers a range of services and levels of care. These type of retirement communities provide social and housing-related services and often also have an assisted living unit and an on-site or affiliated nursing home. Residents may move first into an independent living unit—a private apartment or house—on the campus. If and when residents can no longer live independently in their apartment or home, they move into assisted living or the community’s nursing home.</p>

        <h3>Countable Assets/Countable Income</h3>
        
        <p>Resources that have value and are counted in determining financial eligibility for Medicaid. They include vehicles other than the one used primarily for transportation, life insurance with a face value over $1,500, bank accounts, trusts, and your home, if your spouse or child does not live there, and its equity value is greater than $500,000 (in some states up to $750,000).</p>
        
        <p class="arrow"><a href="#content">Back to Top</a></p>

        <h3 id="D">Daily Maximum (or Daily Benefit Maximum)</h3>
        
        <p>Specified dollar amount that is the maximum a long-term care insurance policy will pay per day for covered services. Policies may pay the full daily maximum regardless of the cost of care or may pay a percent of actual expenses up to the specified daily maximum amount. Some policies specify a single daily maximum for all covered services (for example, nursing home care, assisted living facility, home care) and other policies have one daily maximum for nursing home care and a lower amount for other covered services.</p>

        <h3>Dementia</h3>
        
        <p>A disorder of the brain that can result in deterioration of intellectual and cognitive abilities beyond what might be expected from normal aging.</p>

        <h3>Disability Insurance</h3>
        
        <p>A form of health insurance that provides periodic payments when the person who is insured is not able to work because of sickness or injury.</p>

        <h3>Disability Method</h3>
        
        <p>Method of paying long-term care insurance benefits that only requires you to meet the benefit eligibility criteria. Once you do, you receive your full daily benefit.</p>

        <h3>Do Not Resuscitate Order (DNR)</h3>
        
        <p>Written order from a doctor that resuscitation should not be attempted if a person suffers cardiac or respiratory arrest. A DNR order may be established on the basis of an advance directive from the patient or, from someone entitled to make decisions on the patient’s behalf. Any person who does not wish to undergo lifesaving treatment in the event of cardiac or respiratory arrest can get a DNR order, although DNR is more commonly done when a person with a fatal illness wishes to die without painful or invasive medical procedures.</p>

        <h3>Dollar-for-Dollar Resource Protection</h3>
        
        <p>A benefit available to those who purchase and use a Texas Long-Term Care Partnership long-term care insurance policy. For every dollar a Partnership-qualified policy pays out in benefits, a dollar of the policyholder’s financial resources will be disregarded or set aside when determining if the policyholder qualifies for Medicaid.</p>

        <h3>Durable Power of Attorney</h3>
        
        <p>Legal document that gives someone else the authority to act on your behalf on matters that you specify. The power can be specific to a certain task or broad to cover many financial duties. The power can be given to start immediately or upon mental incapacity. To be valid, the document must be signed by you before you become disabled. Also called a medical power of attorney.</p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="E">Elimination Period</h3>
        
        <p>Specified amount of time at the beginning of a disability during which services covered by a long-term care insurance policy are received, but for which the policy will not pay benefits. Also known as a deductible period or benefit waiting period. </p>

        <h3>Equity Value</h3>
        
        <p>Fair market value of property minus any encumbrances on the property such as mortgages or loans.</p>

        <h3>Estate Recovery</h3>
        
        <p>The process by which Medicaid recovers an amount of money from the estate of a person who received long-term care services through Medicaid. The amount Medicaid recovers cannot be greater than the amount it spent on the person’s medical care.</p>

        <h3>Expense Incurred Method</h3>
        
        <p>Most common method of paying long-term care insurance benefits. Your policy or certificate will pay benefits when you receive eligible services. Once you are charged an expense for a service covered by your policy, benefits are paid either to you or your provider. The coverage pays for whatever amount is less: the expense you incurred or the dollar limit of your policy. Also called reimbursement method.</p>
		
        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="F">Facility-Based Services</h3>
        
        <p>Personal care provided in a fee-based facility, such as adult foster care, assisted living centers, continuing care retirement communities, and nursing homes.</p>

        <h3>Free-Look Period</h3>
        
        <p>Typically, this is a 30-day period following receipt of a long-term care insurance policy during which you may return it for any reason for a full refund of any premiums paid.</p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="G">Group Home</h3>
        
        <p>Private homes that are designed to provide housing, meals, housekeeping, personal care services, and support to residents who are frail or have a disability. At least one caregiver is required to be present at all times. In many states, group homes are licensed or certified and must meet criteria for facility safety, types of services provided, and the number and type of residents for which they can care. Group homes are often owned and managed by an individual or family that is involved in the everyday operation of the home. Also called board and care home.</p>

        <h3>Guaranteed Renewable</h3>
        
        <p>A long-term care insurance policy that cannot be cancelled by an insurance company unless benefits have been exhausted or premiums have not been paid as due. The company cannot change the coverage or refuse to renew coverage for reasons other than nonpayment of premiums. In a guaranteed renewable policy, the insurance company may increase premiums only on an entire class of policies, not just on your policy.</p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="H">Health Care Proxy</h3>
        
        <p>Legal document in which you name someone to make health care decisions for you if, for any reason and at any time, you become unable to make or communicate those decisions for yourself.</p>

        <h3>Home Equity</h3>
        
        <p>The current market value of a home minus any amount you owe on the property through mortgages or loans.</p>

        <h3>Home-Based Services</h3>
        
        <p>Long-term care services provided in your home. These could include short-term services, such as nursing, physical therapy, or other therapies ordered by a doctor for a specific condition. They may also include help with basic activities such as bathing and dressing, and homemaker and chore services such as meal preparation and routine household chores.</p>

        <h3>Homemaker/Chore Services</h3>
        
        <p>Homemaker/chore services can help you with general household activities such as meal preparation, routine household care, and heavy household chores such as washing floors and windows, or lawn maintenance.</p>

        <h3>Hospice Care/Services</h3>
        
        <p>Short-term, supportive care for the terminally ill (life expectancy of six months or less) that focuses on pain management, and emotional, physical, and spiritual support for the patient and family. It can be provided at home, or in a hospital, nursing home, or hospice facility. Hospice care is typically paid for by Medicare and is not usually considered long-term care.</p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="I">Incontinence</h3>
        
        <p>Inability to maintain control of bowel and bladder functions; or when unable to maintain control of these functions, the inability to perform associated personal hygiene (including caring for a catheter or colostomy bag). Continence is one of the six activities of daily living used as a means of determining eligibility for public programs such as Medicaid long-term care services.</p>

        <h3>Indemnity Method</h3>
        
        <p>Method of paying a set dollar amount for benefits regardless of the cost of the services delivered. The insurance company decides if you are eligible for benefits and if the services you receive are covered under the policy. The cost of specific services is not important in determining the amount of benefits paid. The insurance company pays benefits directly to you up to the limit of the policy.</p>

        <h3>Inflation Protection Feature</h3>
        
        <p>Feature of a long-term care insurance policy by which benefits increase over time to help offset future increases in service costs. All long-term care insurance policies must offer a 5 percent compound annual inflation protection rider as an option. </p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="L">Life Settlement</h3>
        
        <p>Generally refers to a policy owner selling a life insurance policy to third-party investors for less than the face value but more than cash-surrender value of the policy.</p>

        <h3>Living Will</h3>
        
        <p>Legal document used to specify whether you would like to be kept on artificial life support if you become permanently unconscious or are dying and unable to speak for yourself. It also specifies other aspects of health care you would like under those circumstances. Also called health care directive, advanced health care directive, or advance directive.</p>

        <h3>Long-Term Care</h3>
        
        <p>Variety of services and supports to meet health or personal care needs over an extended period of time.</p>

        <h3>Long-Term Care Facility</h3>
        
        <p>Licensed facility that provides general nursing care to those who are chronically ill or unable to take care of daily living needs. Also called nursing home or convalescent care facility.</p>

        <h3>Long-Term Care Insurance</h3>
        
        <p>Specific type of insurance policy designed to offer financial support to pay for necessary long-term care services.</p>

        <h3>Long-Term Care Services</h3>
        
        <p>ervices that include medical and non-medical care to people with a chronic illness or disability – car that meets a person’s health or personal needs. Most long-term care helps people with activities of daily living, such as dressing, bathing, and using the bathroom. Long-term care can be provided at home, in the community, or in a facility. The definition is slightly different for the purposes of Medicaid eligibility and payment: Long-term care services are those provided to someone requiring a level of care equivalent to that received in a nursing facility.</p>

        <h3>Look-Back Period</h3>
        
        <p>A Medicaid term that refers to the block of time in which a person applying for Medicaid cannot transfer any of their resources in order to become eligible for Medicaid. </p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="M">Medicaid</h3>
        
        <p>Joint federal and state public assistance program for financing health care for low-income families, children, related caretakers of dependent children, pregnant women, people age 65 and older, and adults and children with disabilities. </p>

        <h3>Medical Necessity</h3>
        
        <p>A medical condition that requires the type of care provided in a long-term care facility, such as a nursing home or through home- and community-based services.</p>

        <h3>Medical Power of Attorney</h3>
        
        <p>Legal document that allows you to name someone to make health care decisions for you if, for any reason and at any time, you become unable to make or communicate those decisions for yourself.</p>

        <h3>Medicare</h3>
        
        <p>Federal program that provides hospital and medical expense benefits for people over age 65, or those meeting specific disability standards. Benefits for nursing home and home health services are limited.</p>

        <h3>Medicare Supplement Insurance (Medigap)</h3>
        
        <p>Private insurance policy that covers gaps in Medicare coverage.</p>

        <h3>Memory Care (also called Alzheimer’s special care)</h3>
        
        <p>Services designed to meet the needs of individuals with Alzheimer’s or other forms of dementia. Memory care may be provided in standalone residential facilities or in separate units that are part of larger facilities. Memory care typically includes a higher level of security, specialized activities, and trained staff.</p>

        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="N">Nonforfeiture Benefits</h3>
        
        <p>Long-term care insurance policy feature that refunds at least part of the premiums to you if you cancel your policy or let it lapse.</p>

        <h3>Nursing Home</h3>
        
        <p>Licensed facility that provides general nursing care to those who are chronically ill or unable to take care of daily living needs. Also called long-term care facility or convalescent care facility.</p>
		
        <p class="arrow"><a href="#content">Back to Top</a></p>
        
        <h3 id="P">Partnership Policy</h3>
        
        <p>Private long-term care insurance policy that comes with an added benefit: Dollar-for-dollar resource protection – which allows you to keep some or all of your assets if you apply for Medicaid after using up your policy’s benefits. If the policyholder has to apply for Medicaid after using the benefits of the Partnership policy, Medicaid will set aside or disregard the policyholder’s resources in the dollar amount that matches what the Partnership policy paid out in benefits.</p>

        <h3>Personal Care</h3>
        
        <p>Non-skilled service, for help with things like bathing, dressing, eating, getting in and out of bed or chair, moving around, and using the bathroom. May also include care that most people do themselves, such as using eye drops. Personal care is considered a home-based service. This is also called custodial care.</p>

        <h3>Pre-Existing Condition</h3>
        
        <p>A condition for which the policyholder sought or received medical treatment within a specified time prior to the effective date of insurance coverage. </p>

        <h3>Pre-Existing Condition Exclusion</h3>
        
        <p>A policy provision that excludes coverage for a time following the effective date of coverage (for example, 6 to 12 months) if the care is needed for a pre-existing condition. </p>

        <h3>Private Pay</h3>
        
        <p>Paying for long-term care with your personal finances, such as savings and investments, long-term care insurance, home equity, certain life insurance benefits and/or options, certain annuity contracts, and trusts.</p>

        <h3>Proxy</h3>
        
        <p>A person authorized to act for another.</p>
        
        <p class="arrow"><a href="#content">Back to Top</a></p>

        <h3 id="R">Rate Class</h3>
        
        <p>A group of customers with similar characteristics that is assigned a rate unique from the rates of other groups.</p>

        <h3>Refund of Premium</h3>
        
        <p>Amount received by the policyholder if the policy is canceled, benefits are reduced, or the premium is reduced.</p>

        <h3>Restoration of Benefits</h3>
        
        <p>A provision in some long-term care insurance plans which restores a person’s lifetime maximum benefit amount in small increments. A restoration of benefits only occurs if you don’t need long-term care services for a specified period of time after you have received benefits.</p>

        <h3>Reverse Mortgage</h3>
        
        <p>Type of loan based on home equity that enables older homeowners (62 years or older) to convert part of the equity in their homes into tax-free income without having to sell the home, give up title, or take on a new monthly mortgage payment. Instead of making monthly payments to a lender, as with a regular mortgage, a lender makes payments to the homeowner. The loan, along with financing costs and interest on the loan, does not need to be repaid until the homeowner dies or no longer lives in the home.</p>

        <h3>Rider</h3>
        
        <p>Addition to an insurance policy that changes the provisions of the policy. The rider is added at the request of the person applying for insurance or, in the cases of an existing policy, the person insured. </p>
        
        <p class="arrow"><a href="#content">Back to Top</a></p>

        <h3 id="S">Sale-Leaseback</h3>
        
        <p>This is also called short for sale-and-leaseback. A financial transaction where a person sells an asset, such as a home, and leases it back from the new owner.</p>

        <h3>Skilled Care</h3>
        
        <p>Nursing care that provides help with medications, caring for wounds and therapies (such as occupational, speech, respiratory, and physical therapy). Skilled care usually requires the services of a licensed professional such as a nurse, doctor, or therapist.</p>

        <h3>Spend Down</h3>
        
        <p>This is a federal requirement that a person use most of his or her income and resources to meet Medicaid eligibility requirements.</p>

        <h3>Standby Assistance</h3>
        
        <p>A caregiver that doesn’t physically help a person perform activities of daily living but instead stays close by to watch over the person and provide physical assistance if necessary. Someone who does not need hands-on help might need standby assistance if they are unsteady or have physical movement limitations.</p>

        <h3>State-to-State Coverage</h3>
        
        <p>An agreement with other states and the federal government to make the Medicaid-related benefits of a Partnership-qualified policy effective in other states. Also called reciprocity.</p>

        <h3>Substantial Supervision</h3>
        
        <p>Presence of person directing and watching over another person who has a cognitive impairment.</p>
        
