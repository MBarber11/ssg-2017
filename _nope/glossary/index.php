        <h3>Beneficios acelerados por muerte</h3>
        
        <p>Una opción de la póliza de seguro que le permite usar algunos de los beneficios por muerte de la póliza antes de que esta ocurra.</p>

        <h3>Beneficios de incaducidad</h3>
        
        <p>Una característica de la póliza de seguro de cuidados a largo plazo que le reembolsa por lo menos una parte de las primas si su póliza caduca o se cancela.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="C">Capital acumulado de una casa</h3>
        
        <p>El valor de mercado actual de una propiedad menos cualquier cantidad que deba contra la propiedad por hipotecas o préstamos.</p>
        
        <h3>Casa de alojamiento y cuidados</h3>
        
        <p>Casas residenciales privadas que ofrecen servicios de vivienda, comida, tareas del hogar, cuidado personal y apoyo a los residentes cuya salud es delicada o que tienen una discapacidad.  Por lo menos un proveedor de cuidados está presente en todo momento.  En muchos estados, las casas de alojamiento y cuidados están acreditadas o certificadas y tienen que cumplir con los requisitos de seguridad de las instalaciones, de los tipos de servicios que se ofrecen y del número y tipo de residentes a los que pueden cuidar.  Las casas de alojamiento y cuidados son operadas y manejadas, la mayoría de las veces, por un individuo o una familia involucrados en las operaciones diarias de la casa. También se conocen como casas para grupos o centros residenciales de cuidados.</p>

        <h3>Casa de reposo</h3>
        
        <p>Un centro acreditado que ofrece cuidados generales de enfermería para personas que sufren de una enfermedad crónica o que no pueden realizar las actividades diarias de vida. También se le conoce como centro de cuidados a largo plazo o centro de cuidado para convalecientes.</p>
        
        <h3>Casas para grupos</h3>
        
        <p>Casas privadas diseñadas para ofrecer vivienda, comida, limpieza, servicios de cuidado personal y apoyo a los residentes que tienen una salud delicada o alguna discapacidad.  Por lo menos un proveedor de cuidados está presente en todo momento.  En muchos estados, las casas para grupos están acreditadas o certificadas y tienen que cumplir con los requisitos de seguridad de las instalaciones, los tipos de servicios que se ofrecen y el número y tipo de residentes a los que pueden cuidar.  Las casas para grupos son operadas y manejadas a menudo por un individuo o una familia involucrados en las operaciones diarias de la casa. También se les conoce como casas de alojamiento y cuidados.</p>
        
        <h3>Centro de cuidados a largo plazo</h3>
        
        <p>Un centro acreditado que ofrece cuidados generales de enfermería para personas que sufren de una enfermedad crónica o que no pueden realizar las actividades de la vida diaria. También se conoce como casa de reposo o centro de cuidado para convalecientes.</p>

        <h3>Centro de vida asistida</h3>
        
        <p>Centro de vivienda residencial que ofrece servicios de cuidado personal, ayuda con las actividades de la vida diaria, con medicamentos y servicios como limpieza y lavandería. Estos centros también ofrecen cuidados de atención médica y de salud, pero los cuidados no son tan intensivos como los que se ofrecen en una casa de reposo. Los centros de vida asistida les permiten a las personas mantenerse relativamente independientes.  El tipo y tamaño de los centros varían, y pueden ser desde casas pequeñas hasta apartamentos amplios tipo complejo. Los niveles de cuidado y los servicios también varían.</p>
        
        <h3>Cláusula adicional</h3>
        
        <p>Una adición a una póliza de seguro que cambia las disposiciones de la póliza.  La cláusula se añade a petición de la persona que está solicitando el seguro o, en caso de que exista una póliza, de la persona asegurada.</p>
        
        <h3>Cobertura de estado a estado</h3>
        
        <p>Un acuerdo entre otros estados y el gobierno federal para permitir que los beneficios de una póliza calificada por la Asociación relacionados con Medicaid sean efectivos en otros estados. También se le conoce como reciprocidad.</p>
        
        <h3>Comunidades de retiro de cuidado continuo (CCRC)</h3>
        
        <p>Complejo para personas jubiladas que ofrece una gran variedad de servicios y niveles de cuidado.  Este tipo de comunidades de jubilación ofrece servicios sociales o de vivienda y, a veces, también tiene una unidad de vida asistida y una casa de reposo en el mismo lugar.  Los residentes pueden mudarse primero a una unidad de vivienda independiente, que puede ser un apartamento privado o una casa, en las instalaciones.  Cuando los residentes no pueden vivir independientemente en una casa o un apartamento, se mudan a un centro de vida asistida o a la casa de reposo de la comunidad.</p>
        
        <h3>Condición preexistente</h3>
        
        <p>Una condición por la cual el dueño de la póliza recibió tratamiento médico por un período específico de tiempo antes de la fecha efectiva de la cobertura del seguro.</p>
        
        <h3>Contrato de venta con arrendamiento posterior al vendedor (sale-leaseback)</h3>
        
        <p>Una transacción financiera en la cual una persona vende un bien, como su casa, y se la alquila al nuevo dueño.</p>
        
        <h3>Cuidado de trastornos de la memoria (también conocido como cuidado especializado para Alzheimer)</h3>
        
        <p>Servicios diseñados para satisfacer las necesidades de individuos con Alzheimer u otra forma de demencia. El cuidado de trastornos de la memoria se puede ofrecer en centros residenciales o en unidades por separado que forman parte de centros más grandes. El cuidado de trastornos de la memoria incluye regularmente un nivel de seguridad más alto, actividades especializadas y personal capacitado.</p>
        
        <h3>Cuidado especializado</h3>
        
        <p>Cuidado de enfermería que ofrece ayuda con medicamentos, cuidado de heridas y terapias (como ocupacional, del habla, respiratoria y física).  El cuidado especializado usualmente requiere de los servicios de un profesional con licencia como una enfermera, un doctor o un terapeuta.</p>
        
        <h3>Cuidado personal</h3>
        
        <p>Servicios de cuidado no especializados que ofrecen ayuda con actividades como bañarse, vestirse, comer, sentarse o levantarse de la cama a una silla, desplazarse por la casa o usar el baño. También puede incluir los cuidados que la mayoría de las personas hacen por su cuenta, como ponerse gotas para los ojos.  Los servicios de cuidado personal están considerados como un servicio de salud en casa. También se conocen como cuidados asistenciales.</p>
        
        <h3>Cuidado temporal para adultos</h3>
        
        <p>Es un arreglo de vida en el que las personas que ya no pueden vivir de forma independiente en sus casas debido a limitaciones físicas, mentales o emocionales reciben supervisión las 24 horas del día.  Las personas que ofrecen cuidado temporal de adultos viven en el mismo hogar que los residentes y comparten un área en común.</p>
        
        <h3>Cuidador</h3>
        
        <p>Un cuidador es una pareja, un familia, amigo o vecino que ayuda con el cuidado de alguien de edad avanzada o que tiene una discapacidad y vive en su propia casa.</p>
        
        <h3>Cuidados de salud en casa</h3>
        
        <p>Servicios de cuidados a largo plazo que se ofrecen en su casa. Estos servicios pueden incluir servicios a corto plazo, como enfermería, fisioterapia u otro tipo de terapia recomendada por un doctor para una condición específica. También pueden incluir ayuda con actividades básicas como bañarse y vestirse, y tareas de ama de casa como preparación de comidas y las tareas rutinarias del hogar.</p>
        
        <h3>Cuidados a largo plazo</h3>
        
        <p>Una variedad de servicios y apoyos para satisfacer las necesidades personales o de salud por un período largo de tiempo.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>

        <h3 id="D">Demencia</h3>
                
        <p>Un trastorno cerebral que puede causar un deterioro de las habilidades cognitivas e intelectuales más grave de lo que normalmente se esperaría del envejecimiento regular.</p>

        <h3>Discapacidad cognitiva</h3>
        
        <p>Deficiencia en ciertas funciones del cerebro: memoria de largo o corto plazo; orientación de persona, lugar o tiempo; razonamiento deductivo o abstracto; o del buen juicio, cuando se relaciona con estar consciente de la propia seguridad. La enfermedad de Alzheimer es un ejemplo de una discapacidad cognitiva.</p>
 	
    	<p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="E">Enfermedad crónica</h3>
        
        <p>Una enfermedad de larga duración o recurrente que causa que una persona necesite ayuda con actividades de la vida diaria y, a veces, otros servicios de salud y de apoyo.  Algunos ejemplos de una enfermedad crónica son la enfermedad de Parkinson o enfermedad de Alzheimer.  Se espera que, como mínimo, la condición o enfermedad dure por lo menos 90 días consecutivos.  Este término se usa en una póliza de seguro de cuidados a largo plazo exenta de impuestos para describir a una persona que necesita cuidados a largo plazo por su incapacidad de hacer cierto número de actividades de la vida diaria sin ayuda, o porque tiene una discapacidad cognitiva severa.</p>
       
		<h3>Enfermedad de Alzheimer</h3>
        
        <p>Una forma degenerativa y progresiva de demencia que causa deterioro intelectual severo. Su primer síntoma es el deterioro de la memoria, seguido del deterioro del pensamiento y del habla.</p>
        
        <h3>Exclusión de condición preexistente</h3>
        
        <p>Una disposición de la póliza que excluye la cobertura por un tiempo posterior a la fecha efectiva de cobertura (por ejemplo, de 6 a 12 meses) si se necesitan cuidados para una condición preexistente.</p>
        
        <h3>Exoneración de prima</h3>
        
        <p>Una característica de la póliza de seguro de cuidados a largo plazo que suspende los pagos de la prima durante el tiempo en que el dueño de la póliza recibe beneficios de servicios de cuidados a largo plazo.  Esta suspensión es usualmente por un período específico de tiempo y continúa hasta que la persona ya no necesite servicios de cuidados a largo plazo; cuando esto ocurre, los pagos de la prima continúan.</p>
	
    	<p class="arrow"><a href="#content">Regresar al Principio</a></p>

        <h3 id="F">Fideicomiso para uso caritativo de bienes remanentes</h3>
        
        <p>Fideicomiso irrevocable especial exento de impuestos que se prepara para cumplir con las leyes y regulaciones de impuestos federales.  Con este tipo de fideicomiso usted puede transferir dinero en efectivo o bienes al fideicomiso y podría recibir algún ingreso de por vida o por un número específico de años (que no exceda los 20 años).  La tasa mínima para el pago es del 5 por ciento, y la máxima es un 50 por ciento.  Cuando usted muera, la cantidad que quede en el fideicomiso va a una organización sin fines de lucro designada para recibir la cantidad como parte de los arreglos del fideicomiso.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="G">Grupo con tarifa especial</h3>
        
        <p>Un grupo de clientes con características similares al que se le asigna una tarifa única y diferente a la tarifa de otros grupos.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="H">Hipoteca revertida</h3>
        
        <p>Un tipo de préstamo que se basa en el capital acumulado de la casa que permite que los adultos mayores (de 62 años de edad en adelante) dueños de una casa conviertan parte del capital acumulado de sus viviendas en ingresos libres de impuestos sin tener que vender la casa, ceder su título de propiedad ni adquirir un pago de hipoteca mensual nuevo.  En lugar de hacer pagos mensuales a un prestamista, como ocurre con una hipoteca regular, el prestamista le hace pagos al dueño de la propiedad. El préstamo, junto con los costos de financiamiento y los intereses del préstamo, no necesita pagarse hasta que el dueño de la propiedad muera o no viva más en la propiedad.</p>
		
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>

        <h3 id="I">Incontinencia</h3>
        
        <p>La inhabilidad de mantener el control de las funciones del intestino o de la vejiga; o bien, cuando no se puede mantener el control de estas funciones, la incapacidad de mantener la correspondiente higiene personal (incluyendo el cuidado de un catéter o de una bolsa de desechos de una colostomía). La continencia es una de seis actividades de la vida diaria que se toma en cuenta para determinar si una persona califica para los programas públicos como los cuidados a largo plazo de Medicaid.</p>
        
        <h3>Instrucciones por anticipado</h3>
        
        <p>Es un documento legal que se usa para notificarles a los profesionales médicos si desea que le den la alternativa de mantenerlo con vida artificial si queda inconsciente permanentemente o está muriendo y no puede hablar por sí mismo. También especifica otros aspectos de la atención médica que usted quisiera recibir bajo estas circunstancias. Este documento también se conoce como instrucciones para el cuidado de la salud, instrucciones anticipadas para el cuidado de la salud o testamento vital.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>
       
        <h3 id="L">Liquidación del seguro de vida</h3>
        
        <p>Generalmente se refiere al dueño de la póliza que vende una póliza de seguro de vida a un inversionista independiente por menos del valor nominal, pero por más del valor de rescate en efectivo de la póliza.</p>
        
        <h3>Look-Back Period</h3>
        
        <p>Un término de Medicaid que se refiere al tiempo en el que una persona que solicita beneficios de Medicaid no puede transferir sus recursos con el fin de poder calificar para Medicaid.</p>

		<p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="M">Máximo diario (también conocido como beneficio máximo diario)</h3>
        
        <p>Cantidad específica de dinero que es el máximo que la póliza de seguro de cuidados a largo plazo pagará por día por los servicios cubiertos. Las pólizas podrían pagar la cantidad diaria máxima completa sin importar el costo del cuidado o pueden pagar un porcentaje de los gastos que no excedan la cantidad diaria máxima específica. Algunas pólizas especifican una cantidad única máxima por todos los servicios cubiertos (por ejemplo, cuidados en casa de reposo, centro de vida asistida, cuidados de salud en casa), y otras pólizas tienen un diario máximo para cuidados en una casa de reposo y una cantidad menor para otros servicios cubiertos.</p>
        
        <h3>Medicaid</h3>
        
        <p>Programa de asistencia pública federal y estatal para financiar la atención médica de familias y niños de bajos recursos, familiares que cuidan a niños dependientes, mujeres embarazadas, personas de 65 años o mayores y adultos y niños con discapacidades. </p>
        
        <h3>Medicare</h3>
        
        <p>Programa federal que ofrece beneficios para los gastos médicos y hospitalarios de personas mayores de 65 años de edad, o que cumplen con los parámetros específicos de discapacidad.  Los beneficios para casas de reposo o cuidados de salud en casa son limitados.</p>
        
        <h3>Método de gastos incurridos</h3>
        
        <p>Es el método que más se usa para pagar beneficios del seguro de cuidados a largo plazo.  Su póliza o certificado pagará los beneficios cuando usted reciba servicios que califiquen.  Una vez le cobren un gasto por un servicio cubierto por su póliza, se le pagarán los beneficios a usted o a un proveedor.  La cobertura paga lo que sea menor: el gasto en el que usted incurrió o el límite de dinero de su póliza.  Este método también se conoce como método de reembolso.</p>
        
        <h3>Método de indemnización</h3>
        
        <p>Método en el que se paga una cantidad establecida de dinero por los beneficios sin importar el costo de los servicios ofrecidos.  La compañía de seguros decide si usted califica para beneficios y si los servicios que recibe están cubiertos por la póliza.  El costo de servicios específicos no es importante para determinar la cantidad de beneficios que se pagarán.  La compañía de seguros le paga directamente  a usted los beneficios sin rebasar el límite establecido de la póliza.</p>
        
        <h3>Método por discapacidad</h3>
        
        <p>El método que paga beneficios de seguro de cuidados a largo plazo cuyo único requisito es que usted cumpla con los criterios para ser elegible. Una vez cumpla con los requisitos, recibe el beneficio diario completo.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="N">Necesidad médica</h3>
        
        <p>Una condición médica que requiere el tipo de cuidados que se ofrecen en un centro de cuidados a largo plazo, como una casa de reposo, o mediante servicios que se ofrecen en la casa y la comunidad.</p>

		<p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="O">Orden de no resucitación (DNR)</h3>
        
        <p>Orden escrita por un doctor que dice que no debe resucitarse a la persona si sufre de un paro cardíaco o respiratorio.  Puede establecerse una orden de no resucitación (DNR, por su sigla en inglés) con base en las instrucciones por anticipado del paciente o de alguien que pueda tomar decisiones a nombre del paciente.  Cualquier persona que no quiera un tratamiento que le salve la vida si sufre un paro respiratorio o cardíaco puede obtener una orden de no resucitación, si bien este tipo de orden de no resucitación es más común cuando una persona con una enfermedad mortal desea morir sin procedimientos médicos invasivos y dolorosos.</p>

		<p class="arrow"><a href="#content">Regresar al Principio</a></p>
        
        <h3 id="P">Pago privado</h3>
        
        <p>El pago de los cuidados a largo plazo con sus bienes personales, como ahorros e inversiones, seguro de cuidados a largo plazo, capital acumulado de su casa, ciertos beneficios de un seguro de vida u otras opciones, ciertos contratos de anualidades y fideicomisos.</p>
        
        <h3>Periodo de eliminación</h3>
        
        <p>Período de tiempo específico al comenzar una discapacidad durante el cual se reciben los servicios cubiertos por una póliza de seguro de cuidados a largo plazo, pero la póliza no paga beneficios.  Este período también se conoce como período deducible o período de espera de beneficios.</p>
        
        <h3>Periodo de espera de beneficios</h3>
        
        <p>Cantidad de tiempo específica al comienzo de una discapacidad durante la cual se reciben los servicios cubiertos, pero la póliza no paga los beneficios. También se conoce como período deducible o período de eliminación.</p>
        
        <h3>Periodo de prueba gratuito</h3>
        
        <p>Típicamente, un período de 30 días después de recibir una póliza de seguro de cuidados a largo plazo durante el cual se puede devolver dicha póliza por cualquier razón y recibir un reembolso completo de las primas que se hayan pagado.</p>
        
        <h3>Planificación de cuidados para el futuro</h3>
        
        <p>Conocer las opciones y  planificar los servicios de cuidados para el final de la vida.</p>
        
        <h3>Poder notarial duradero</h3>
        
        <p>Documento legal que le da a alguien la autoridad para actuar en su nombre en los asuntos que especifique.  El poder puede ser específico para ciertas tareas o abierto a la cobertura de diversas obligaciones financieras.  El poder puede comenzar de inmediato o en cuanto surja una discapacidad mental.  Para que sea válido, el documento tiene que estar firmado por usted antes de que sufra una discapacidad.  Este documento también es conocido como poder notarial para asuntos médicos.</p>
        
        <h3>Poder notarial para asuntos médicos</h3>
        
        <p>Un documento legal que le permite nombrar a alguien para tomar decisiones de atención médica por usted si, por alguna razón y en cualquier momento, usted no puede tomar o comunicar decisiones por sí mismo.</p>
        
        <h3>Póliza de la Asociación</h3>
        
        <p>Póliza de seguro privada de cuidados a largo plazo que tiene beneficios añadidos, como protección de recursos dólar por dólar, que permite mantener cierta cantidad de sus bienes si solicita beneficios de Medicaid después de haber agotado los beneficios de su póliza.  Si el dueño de la póliza tiene que solicitar beneficios de Medicaid luego de haber agotado los beneficios de la póliza de la Asociación, Medicaid mantendrá aparte o no tomará en cuenta los recursos del dueño de la póliza por la cantidad de dinero que iguale a la cantidad que la póliza de la Asociación pagó en beneficios.</p>
        
        <h3>Póliza de seguro de cuidados a largo plazo exenta de impuestos</h3>
        
        <p>Póliza de seguro de cuidados a largo plazo que cumple con ciertos parámetros de las leyes federales y ofrece ciertas ventajas relacionadas con los impuestos federales.</p>
        
        <h3>Primas no devengadas</h3>
        
        <p>Una prima que el seguro paga por adelantado.</p>
        
        <h3>Protección contra la inflación</h3>
        
        <p>Característica de una póliza de seguro de cuidados a largo plazo con la cual los beneficios aumentan con el tiempo para compensar por aumentos futuros en los costos de los servicios.  Todas las pólizas de seguro de cuidados a largo plazo tienen que ofrecer una cláusula de un 5 por ciento anual compuesto de protección contra la inflación como alternativa. </p>
        
        <h3>Protección de recursos dólar por dólar</h3>
        
        <p>Un beneficio disponible para las personas que compran o usan una póliza de seguro de cuidados a largo plazo de la Asociación de Cuidados a Largo Plazo de Texas.  Por cada dólar que las pólizas calificadas por la Asociación paguen en beneficios, un dólar de sus recursos financieros se protegerá o mantendrá aparte al momento de determinar si el dueño de la póliza califica para recibir beneficios de Medicaid.</p>
        
        <p class="arrow"><a href="#content">Regresar al Principio</a></p>        

		<h3 id="R">Recuperación de propiedad </h3>
        
        <p>Proceso por el cual Medicaid recurre a las propiedades de una persona para recuperar la cantidad de dinero que dicha persona recibió en servicios de cuidados a largo plazo a través de Medicaid.  La cantidad que Medicaid recupera no puede ser mayor que la cantidad que invirtió en el cuidado médico de la persona.</p>
        
        <h3>Recursos contables/ingresos contables</h3>
        
        <p>Recursos que tienen valor y son contables para determinar la elegibilidad financiera para recibir beneficios de Medicaid.  Estos incluyen automóviles que no sean su transportación primaria, seguro de vida con un valor nominal de $1,500, cuentas de banco, fideicomisos y su casa, si su esposo(a) o hijos no viven en ella y el valor de la propiedad es mayor de $500,000 (en algunos estados, hasta $750,000).</p>
        
        <h3>Reembolso de prima</h3>
        
        <p>Cantidad que recibe el dueño de la póliza si se cancela la póliza, se reducen los beneficios o se reduce la prima.</p>
        
        <h3>Renovación garantizada</h3>
        
        <p>Una póliza de seguro de cuidados a largo plazo que no puede ser cancelada por una compañía de seguros a menos que los beneficios se hayan agotado o que las primas no se hayan pagado a tiempo.  La compañía no puede cambiar la cobertura o rehusarse a renovar la cobertura por ninguna razón que no sea la falta de pago de las primas.  En una póliza de renovación garantizada, la compañía de seguros puede incrementar la prima solamente como parte de una clase completa de pólizas, pero no únicamente en su póliza.</p>
        
        <h3>Representante</h3>
        
        <p>Una persona autorizada para actuar en nombre de otra.</p>

        <h3>Representante para asuntos médicos</h3>
        
        <p>Un documento legal en el cual usted nombra a alguien para que tome decisiones médicas por usted si por alguna razón, y en algún momento dado, usted no puede tomar o comunicar estas decisiones por sí mismo.</p>
        
        <h3>Responsabilidad económica</h3>
        
        <p>Requisito federal que establece que una persona debe usar la mayoría de sus ingresos y recursos para poder cumplir con los requisitos de elegibilidad de Medicaid.</p>
        
        <h3>Resucitación cardiopulmonar (CPR)</h3>
        
        <p>Una combinación de respiración de rescate (resucitación boca a boca) y compresiones de pecho que se usa cuando la respiración o la circulación sanguínea de una persona no funciona correctamente. La resucitación cardiopulmonar (CPR) puede restaurar la circulación de la sangre rica en oxígeno que va al cerebro.</p>
        
        <h3>Restauración de beneficios</h3>
        
